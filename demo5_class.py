# 2. 类的方式定义
class Calc():
    # 加法
    def add(x, y):
        return x + y

    # 减法
    def sub(x, y):
        return x - y

    # 乘法
    def mul(x, y):
        return x * y

    # 除法
    def div(x, y):
        return x / y